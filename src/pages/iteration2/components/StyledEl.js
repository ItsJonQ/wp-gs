export const generatePrimitive = tagName => {
	return React.forwardRef(
		(
			{ withBlockStyles, style: styleProp, blockId, postId, ...props },
			ref
		) => {
			const globalStyles = useStyles({ ...props, blockId, postId });
			const style = withBlockStyles ? globalStyles : null;

			return React.createElement(tagName, { ...props, style, ref });
		}
	);
};

const styledPrimitives = domElements.reduce((collection, tagName) => {
	return {
		...collection,
		[tagName]: generatePrimitive(tagName),
	};
}, {});

export const StyledElement = styledPrimitives;
export const SE = StyledElement;
